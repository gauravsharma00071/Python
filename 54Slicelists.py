# slice test
# formate for list slicing is :
# [start:stop:step]

list = [1,2,3,4,5,6,7]
print(list[:])

# get a specific position

print(list[2:])

# before a specific position
print(list[:2])

# get all the items from one position to another position

print(list[2:4])

# get items at specified intervals
print(list[::2])

print(list[::-2])

print(list[1:4:2])